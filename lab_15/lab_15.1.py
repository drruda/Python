import pandas as pd

# Початкові дані
data = {
    "Шевченко Олександр": [12, 4, 10, 7, 5, 8, 3, 3, 5, 7],
    "Левченко Наталія": [5, 6, 7, 5, 4, 7, 5, 4, 4, 8],
    "Гнатюк Андрій": [12, 10, 9, 10, 5, 7, 8, 5, 12, 10],
    "Романюк Ольга": [12, 8, 9, 10, 9, 8, 7, 9, 10, 8],
    "Сидоренко Ігор": [11, 8, 10, 7, 6, 9, 7, 8, 9, 7],
    "Білий Дмитро": [7, 8, 5, 8, 8, 9, 8, 7, 7, 10],
    "Мельник Катерина": [10, 9, 8, 7, 9, 8, 6, 7, 9, 8],
    "Петров Віктор": [12, 8, 11, 9, 7, 10, 8, 9, 12, 10],
    "Кравчук Анна": [9, 8, 9, 7, 6, 8, 7, 8, 9, 8],
    "Коваленко Марина": [6, 7, 8, 9, 10, 10, 10, 10, 10, 9]
}

# Перетворення у DataFrame
df = pd.DataFrame(data)

# Додаємо новий стовпець із предметами
df['Предмет'] = ['Математика', 'Фізика', 'Хімія', 'Історія', 'Література',
                 'Біологія', 'Географія', 'Інформатика', 'Музика', 'Фізкультура']

# Виведення DataFrame
print("Початковий DataFrame:")
print(df)

# Описова статистика для "Романюк Ольги"
print("\nСтатистика для Романюк Ольги:")
print("Середнє значення оцінок = ", df["Романюк Ольга"].mean())
print("Сума оцінок = ", df["Романюк Ольга"].sum())
print("Мінімальна оцінка = ", df["Романюк Ольга"].min())
print("Максимальна оцінка = ", df["Романюк Ольга"].max())

# Аггрегація: середнє по кожному учню
mean_scores = df.iloc[:, :-1].mean()
print("\nСередній бал кожного учня:")
print(mean_scores)

# Групування за предметом
grouped = df.groupby('Предмет').mean()
print("\nСередні оцінки за предметами:")
print(grouped)
